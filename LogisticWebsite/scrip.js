document.getElementById('click-element').addEventListener('click', function () {
    if (document.getElementById('hiddenText').style.display == 'block') {
        document.getElementById('hiddenText').style.display = 'none';
    }
    else {
        document.getElementById('hiddenText').style.display = 'block';
    }
})

